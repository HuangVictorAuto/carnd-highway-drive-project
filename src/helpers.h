struct Car
{
	double x;				//cars x value
	double y;				//cars y value
	double vx;				//cars velocity in the x direction
	double vy;				// cars velocity in the y direction
	double speed;			//speed of the car
	double s;				//cars frenet s coordinate
	double d;				//cars frenet d coordinate
	int lane;				//lane that the car is in
	double check_car_s;		//where the car will be once the current path is done
  bool init=false; //whether car has been init
};

void sensed_car(vector<Car> &cars,vector<vector<double>> sensor_fusion, int prev_size)
{
  cars.clear();
  for(int i=0;i<sensor_fusion.size();i++)
  {
    if(sensor_fusion[i][6]>0&&sensor_fusion[i][6]<12)
    {
      Car c;
      c.x=sensor_fusion[i][1];
      c.y=sensor_fusion[i][2];
      c.vx=sensor_fusion[i][3];
      c.vy=sensor_fusion[i][4];
      c.s=sensor_fusion[i][5];
      c.d=sensor_fusion[i][6];
      c.speed=sqrt(c.vx*c.vx+c.vy*c.vy);
      c.check_car_s=c.s+((double) prev_size*0.02*c.speed);
      c.lane = (int)floor(c.d / 4.0);
      c.init=true;
      cars.push_back(c);
    }
  }
}

Car front_cloest(vector<Car> &cars,int lane,double car_s)
{
  double distance=9999.0;
  Car close;
  for(Car car: cars)
  {
    if(car.lane==lane&& car.check_car_s-car_s>0 && car.check_car_s-car_s<distance)
    {
      distance=car.check_car_s-car_s;
      close=car;
      close.init=true;
    }
  }
  return close;
}

Car rear_cloest(vector<Car> &cars,int lane,double car_s)
{
  double distance=9999.0;
  Car close;
  for(Car car: cars)
  {
    if(car.lane==lane&& car_s-car.check_car_s>0 && car_s-car.check_car_s<distance)
    {
      distance=car_s-car.check_car_s;
      close=car;
      close.init=true;
    }
  }
  return close;
}

